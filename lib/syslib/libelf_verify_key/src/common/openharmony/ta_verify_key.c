static const struct ta_verify_key g_oh_verify_key[] = {
    { PUB_KEY_2048_BITS, PUB_KEY_RELEASE, &g_ta_verify_pub_key },
};

TEE_Result get_ta_verify_pubkey(struct ta_verify_key *key_info)
{
    return query_ta_verify_pubkey(g_oh_verify_key, sizeof(g_oh_verify_key) / sizeof(g_oh_verify_key[0]), key_info);
}

TEE_Result oh_get_ta_pub_key(void **key, uint32_t alg)
{
    if (key == NULL)
        return TEE_ERROR_BAD_PARAMETERS;
    if (alg == ALG_ECDSA)
        *key = (void *)&g_priv_ca_ta_verify_ecc_key;
    else
        *key = (void *)&g_priv_ca_ta_verify_rsa_key;
    return TEE_SUCCESS;
}
