/*define GPIO maximum group*/
#define GPIO_MAX_GROUP 30
/* support the numbers of irq */
#define GPIO_HANDLER_NUM 2
/* GPIO0 irq base */
#define GPIO_IRQ_BASE 116
/* support sec-gpio number */
#define GPIO_SEC_NUM 31

#define GPIO28_IRQ_NUM 173

#define GPIO0_SE_IRQ_NUM 196

#define GPIO1_SE_IRQ_NUM 195

#define GPIO28_GROUP 28

#define GPIO0_SE_GROUP 29

#define GPIO1_SE_GROUP 30

static unsigned int gpio_get_irq_num(unsigned int gpio_id)
{
	unsigned int group;
	unsigned int irq;

	group = GPIO_TO_GROUP(gpio_id);
	if (group < GPIO28_GROUP) {
		irq = GPIO_IRQ_BASE + group;
	} else if (group == GPIO28_GROUP) {
		irq = GPIO28_IRQ_NUM;
	} else if (group == GPIO0_SE_GROUP) {
		irq = GPIO0_SE_IRQ_NUM;
	} else if (group == GPIO1_SE_GROUP) {
		irq = GPIO1_SE_IRQ_NUM;
	} else {
		HISI_PRINT_ERROR("[gpio]gpio-%d can't get irq num\n", gpio_id);
		return -1;
	}

	return irq;
}

