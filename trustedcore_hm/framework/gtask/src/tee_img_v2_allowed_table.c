TEE_Result tee_secure_img_v2_allowed(const TEE_UUID *uuid)
{
    if (uuid == NULL) {
        tloge("invalid parameter\n");
        return TEE_ERROR_BAD_PARAMETERS;
    }

    uint32_t count = sizeof(g_v2_img_config) / sizeof(g_v2_img_config[0]);
    uint32_t i;

    for (i = 0; i < count; i++) {
        if (TEE_MemCompare(uuid, &g_v2_img_config[i], sizeof(TEE_UUID)) == 0)
            return TEE_SUCCESS;
    }

    return TEE_ERROR_NOT_SUPPORTED;
}
