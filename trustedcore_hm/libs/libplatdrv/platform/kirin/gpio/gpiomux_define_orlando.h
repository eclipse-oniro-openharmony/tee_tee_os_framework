static unsigned int gpio_get_irq_num(unsigned int gpio_id)
{
	unsigned int group;
	unsigned int irq;

	group = GPIO_TO_GROUP(gpio_id);
	if (group < GPIO28_GROUP) {
		irq = GPIO_IRQ_BASE + group;
	} else if (group == GPIO28_GROUP) {
		irq = GPIO28_IRQ_NUM;
	} else if ((group > GPIO28_GROUP) && (group < GPIO31_GROUP)) {
		irq = GPIO29_IRQ_NUM + group - GPIO29_GROUP;
	} else if ((group >= GPIO31_GROUP) && (group <= GPIO33_GROUP)) {
		irq = GPIO31_IRQ_NUM + group - GPIO31_GROUP;
	} else if (group == GPIO0_SE_GROUP) {
		irq = GPIO0_SE_IRQ_NUM;
	} else if (group == GPIO1_SE_GROUP) {
		irq = GPIO1_SE_IRQ_NUM;
	} else {
		HISI_PRINT_ERROR("[gpio]gpio-%d can't get irq num\n", gpio_id);
		return -1;
	}

	return irq;
}

