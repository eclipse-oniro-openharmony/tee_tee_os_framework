static CMSCBB_ERROR_CODE add_crl(CMSCBB_VRF_CTX vrf_ctx, const struct cms_sign_info *sign_info)
{
    CMSCBB_ERROR_CODE ret;

    ret = CmscbbVerifyAddCrl(vrf_ctx, sign_info->crl_buf, sign_info->crl_len);
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: add crl failed (0x%x)", ret);
        return ret;
    }

#ifdef SUPPORT_UPDATE_CRL
    (void)cms_crl_update(sign_info->crl_buf, sign_info->crl_len);
    if (g_device_crl.crl_buf != NULL && g_device_crl.crl_len != 0) {
        ret = CmscbbVerifyAddCrl(vrf_ctx, g_device_crl.crl_buf, g_device_crl.crl_len);
        if (ret != CVB_SUCCESS) {
            tloge("CMSCBB: add device crl failed (0x%x)", ret);
            return ret;
        }
    }
#endif

    return CVB_SUCCESS;
}

TEE_Result check_cms_signature(const struct cms_sign_info *sign_info)
{
    CVB_INT32 result = CVB_FALSE;
    CmscbbCtxCreateParams create_params = {0};
    CMSCBB_VRF_CTX vrf_ctx = NULL;
    CMSCBB_ERROR_CODE ret;

    if (sign_info == NULL) {
        tloge("CMSCBB: invalid param\n");
        return TEE_ERROR_BAD_PARAMETERS;
    }

    /* 1. Creat cmccbb verify ctx */
    create_params.st_size = sizeof(create_params);
    ret = CmscbbVerifyCreateCtx(&vrf_ctx, &create_params);
    if (ret != CVB_SUCCESS || vrf_ctx == NULL) {
        tloge("CMSCBB: create ctx failed (0x%x)", ret);
        return ret;
    }

    /* 2. Add cms root cert */
    ret = CmscbbVerifyAddCert(vrf_ctx, g_cms_root_cert, sizeof(g_cms_root_cert));
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: add cert failed (0x%x)", ret);
        goto EXIT;
    }

    /* 3. Add crl */
    ret = add_crl(vrf_ctx, sign_info);
    if (ret != CVB_SUCCESS)
        goto EXIT;

    /* 4. Verify cms signature */
    ret = CmscbbVerifyDetachSignatureBegin(vrf_ctx, sign_info->cms_buf, sign_info->cms_len);
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: verify signature B failed (0x%x)", ret);
        goto EXIT;
    }

    ret = CmscbbVerifyDetachSignatureUpdate(vrf_ctx, sign_info->ini_buf, sign_info->ini_len);
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: verify signature U failed (0x%x)", ret);
        goto EXIT;
    }

    ret = CmscbbVerifyDetachSignatureFinal(vrf_ctx, &result);
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: verify signature F failed (0x%x)", ret);
    } else if (result != CVB_TRUE) {
        ret = CMSCBB_ERR_PKI_CMS_VERIFY_FAILED;
        tloge("CMSCBB: verify signature F failed (0x%x)", ret);
    }
EXIT:
    (void)CmscbbVrfCtxFree(vrf_ctx);
    return ret;
}

static CMSCBB_ERROR_CODE compare_crl(CMSCBB_VRF_CTX vrf_ctx_update, CMSCBB_VRF_CTX vrf_ctx_device,
    const struct device_crl_t* crl_to_update, CmscbbCrlPeriodStat *crl_stat)
{
    CmscbbCrlBundleInfo* crl_bi_to_update = NULL;
    CmscbbCrlBundleInfo* crl_bi_on_device = NULL;
    CMSCBB_ERROR_CODE ret;

    ret = CmscbbDecodeCrl(vrf_ctx_update,
        (const CVB_BYTE*)(crl_to_update->crl_buf), crl_to_update->crl_len, &crl_bi_to_update);
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: decode crl to update failed (0x%x)", ret);
        return ret;
    }

    if (g_device_crl.crl_buf == NULL)
        ret = CmscbbDecodeCrl(vrf_ctx_device,
            (const CVB_BYTE*)(crl_to_update->crl_buf), crl_to_update->crl_len, &crl_bi_on_device);
    else
        ret = CmscbbDecodeCrl(vrf_ctx_device,
            (const CVB_BYTE*)(g_device_crl.crl_buf), g_device_crl.crl_len, &crl_bi_on_device);
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: decode device crl failed (0x%x)", ret);
        CmscbbCrlFree(vrf_ctx_update, crl_bi_to_update);
        return ret;
    }

    ret = CmscbbCrlCompare(vrf_ctx_update, crl_bi_to_update, crl_bi_on_device, crl_stat);
    if (ret != CVB_SUCCESS)
        tloge("CMSCBB: compare crl failed (0x%x)", ret);

    CmscbbCrlFree(vrf_ctx_update, crl_bi_to_update);
    CmscbbCrlFree(vrf_ctx_device, crl_bi_on_device);
    return ret;
}

static TEE_Result update_crl(const struct device_crl_t *crl_to_update, CmscbbCrlPeriodStat crl_stat)
{
    switch (crl_stat) {
    case SCPS_SAME:
        if (g_device_crl.crl_buf == NULL && g_device_crl.crl_len == 0) {
            tloge("CMSCBB: crl on device update for the first time\n");
            g_device_crl.crl_buf = TEE_Malloc(crl_to_update->crl_len, 0);
            if (g_device_crl.crl_buf == NULL) {
                tloge("CMSCBB: malloc crl on device failed\n");
                return TEE_ERROR_OUT_OF_MEMORY;
            }
            g_device_crl.crl_len = crl_to_update->crl_len;
            (void)memcpy_s(g_device_crl.crl_buf, g_device_crl.crl_len,
                crl_to_update->crl_buf, crl_to_update->crl_len);
            return TEE_SUCCESS;
        } else {
            tloge("CMSCBB: crl to update is same as crl on device\n");
            return CMSCBB_ERR_PKI_CRL_ALREADY_EXIST;
        }
    case SCPS_NEW:
        tloge("CMSCBB: crl to update is newer than crl on device\n");
        TEE_Free(g_device_crl.crl_buf);
        g_device_crl.crl_buf = TEE_Malloc(crl_to_update->crl_len, 0);
        if (g_device_crl.crl_buf == NULL) {
            tloge("CMSCBB: malloc crl on device failed\n");
            return TEE_ERROR_OUT_OF_MEMORY;
        }
        g_device_crl.crl_len = crl_to_update->crl_len;
        (void)memcpy_s(g_device_crl.crl_buf, g_device_crl.crl_len,
            crl_to_update->crl_buf, crl_to_update->crl_len);
        return TEE_SUCCESS;
    case SCPS_OLD:
        tloge("CMSCBB: crl to update is older than crl on device\n");
        return CMSCBB_ERR_PKI_CRL_HAS_EXPIRED;
    case SCPS_MIX:
        tloge("CMSCBB: crl to update is mixed with crl on device\n");
        return CMSCBB_ERR_PKI_CRL_FAILED_MAPPING;
    default:
        tloge("CMSCBB: crl stat is unexcepted\n");
        return TEE_ERROR_EXTERNAL_CANCEL;
    }
}

static CMSCBB_ERROR_CODE do_crl_update(const struct device_crl_t *crl_to_update)
{
    const CVB_BYTE *root_cert = g_cms_root_cert;
    CMSCBB_ERROR_CODE ret;
    CmscbbCrlPeriodStat crl_stat = SCPS_MIX;
    CMSCBB_VRF_CTX vrf_ctx_update = NULL;
    CMSCBB_VRF_CTX vrf_ctx_device = NULL;
    CmscbbCtxCreateParams create_params_update = {0};
    CmscbbCtxCreateParams create_params_device = {0};

    create_params_update.st_size = sizeof(create_params_update);
    create_params_device.st_size = sizeof(create_params_device);

    ret = CmscbbVerifyCreateCtx(&vrf_ctx_update, &create_params_update);
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: create ctx failed (0x%x)", ret);
        return ret;
    }

    ret = CmscbbVerifyAddCert(vrf_ctx_update, root_cert, sizeof(g_cms_root_cert));
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: add cert failed (0x%x)", ret);
        CmscbbVrfCtxFree(vrf_ctx_update);
        return ret;
    }

    ret = CmscbbVerifyCreateCtx(&vrf_ctx_device, &create_params_device);
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: create ctx failed (0x%x)", ret);
        CmscbbVrfCtxFree(vrf_ctx_update);
        return ret;
    }

    ret = CmscbbVerifyAddCert(vrf_ctx_device, root_cert, sizeof(g_cms_root_cert));
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: add cert failed (0x%x)", ret);
        CmscbbVrfCtxFree(vrf_ctx_update);
        CmscbbVrfCtxFree(vrf_ctx_device);
        return ret;
    }

    ret = compare_crl(vrf_ctx_update, vrf_ctx_device, crl_to_update, &crl_stat);
    CmscbbVrfCtxFree(vrf_ctx_update);
    CmscbbVrfCtxFree(vrf_ctx_device);
    vrf_ctx_update = NULL;
    vrf_ctx_device = NULL;
    if (ret != CVB_SUCCESS) {
        tloge("CMSCBB: compare crl failed (0x%x)", ret);
        return ret;
    }

    ret = update_crl(crl_to_update, crl_stat);
    if (ret != TEE_SUCCESS)
        tloge("CMSCBB: update crl failed (0x%x)", ret);

    return ret;
}

TEE_Result cms_crl_update(uint8_t *crl, uint32_t crl_len)
{
    struct device_crl_t crl_to_update = {0};

    if (crl == NULL || crl_len == 0 || crl_len > DEVICE_CRL_MAX) {
        tloge("invalid params\n");
        return TEE_ERROR_BAD_PARAMETERS;
    }

    crl_to_update.crl_buf = crl;
    crl_to_update.crl_len = crl_len;
    return do_crl_update(&crl_to_update);
}
